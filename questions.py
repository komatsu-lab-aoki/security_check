# 方針：
# - リスク判定は「設問ごとに固定」（回答で高/中を変えない）
# - 回答が yes の場合は「問題なし」なので結果表示しない
# - no / unknown はどちらも「対応が必要」扱い（表示する）
# ============================================================

from __future__ import annotations
from typing import Dict, List, Optional, TypedDict


# ----------------------------
# 回答選択肢（UI・ロジック共通）
# ----------------------------
